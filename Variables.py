#  +=  adds a number
#  -=  subtracts a number
#  *=  multiply a number
#  /**= power the number
#  //= divide the number      

score = 2

score += 1
print(score)

score -= 1
print(score)

score *= 2
print(score)

score **= 2
print(score)

score //= 2
print(score)

